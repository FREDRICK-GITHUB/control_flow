fn main() {
    let number = 3;
    //conditions in if statements are sometimes referred to as arms
    //when else if is used, program stops the moment the first true condition is met 
    if number < 5 {
        println!("the condition is true!");
    }if number != 0 {
        println!("the number is not equal to zero!");
    }else{
        println!("the condition is false!");
    }
}
